const store = require('./store');
const { config } = require('./config');

// Bot-language layer. Command KEYWORDS stay English everywhere (help, set,
// took, meal...) — only the bot's replies are localized. The language is
// chosen right after pairing, stored in db.settings.language, and can be
// changed anytime with the "language" command.

const LANGS = {
  en: { name: 'English', locale: 'en-US', aiName: 'English' },
  ar: { name: 'العربية', locale: 'ar-SA', aiName: 'Arabic' },
  zh: { name: '中文', locale: 'zh-CN', aiName: 'Chinese' },
  hi: { name: 'हिन्दी', locale: 'hi-IN', aiName: 'Hindi' },
  es: { name: 'Español', locale: 'es-ES', aiName: 'Spanish' },
  fr: { name: 'Français', locale: 'fr-FR', aiName: 'French' },
  pt: { name: 'Português', locale: 'pt-BR', aiName: 'Portuguese' },
};
const LANG_ORDER = ['en', 'ar', 'zh', 'hi', 'es', 'fr', 'pt'];

function lang() {
  const l = store.db.settings.language || config.language;
  return LANGS[l] ? l : 'en';
}

function setLang(code) {
  if (!LANGS[code]) return false;
  store.db.settings.language = code;
  store.save();
  return true;
}

function locale() {
  return LANGS[lang()].locale;
}

// Language name in English, for the AI meal-analysis prompt
function aiLanguageName() {
  return LANGS[lang()].aiName;
}

// The language menu is always multilingual (shown before a language exists)
function languageMenu() {
  const lines = ['🌐 Choose your language / اختر لغتك:', ''];
  LANG_ORDER.forEach((code, i) => lines.push(`*${i + 1}* — ${LANGS[code].name}`));
  return lines.join('\n');
}

function parseLanguageChoice(text) {
  const t = (text || '').trim().toLowerCase();
  const n = parseInt(t, 10);
  if (n >= 1 && n <= LANG_ORDER.length) return LANG_ORDER[n - 1];
  for (const code of LANG_ORDER) {
    if (t === code || t === LANGS[code].name.toLowerCase()) return code;
  }
  return null;
}

function t(key, vars = {}) {
  const table = T[lang()] || T.en;
  let s = table[key] ?? T.en[key] ?? key;
  return s.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
}

